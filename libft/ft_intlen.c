#include "libft.h"

size_t	ft_intlen(long nb)
{
	size_t	len;

	len = 1;
	if (nb < 0)
	{
		nb = -nb;
		len++;
	}
	while (nb > 9)
	{
		len++;
		nb = nb / 10;
	}
	return (len);
}
