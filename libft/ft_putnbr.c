#include "libft.h"

void	ft_putnbr(long i)
{
	if (i < 0)
	{
		ft_putchar('-');
		i = -i;
	}
	if (i >= 10)
	{
		ft_putnbr(i / 10);
		ft_putchar(i % 10 + '0');
	}
	if (i < 10)
		ft_putchar(i % 10 + '0');
}
